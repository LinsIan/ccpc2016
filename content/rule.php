	<tr><td>
	<div id="contest-rule">
		<h2 class="info-head">競賽規則</h2>
		<div class="info-content">
			<style>
				.rule-list>li {
					padding-bottom: 8px;
				}
			</style>
			<ol class="rule-list">
				<li>參賽以隊為單位，每隊由一至三人組成。一人限參加一隊，不可重覆報名，<span style="color:red;">每校各系限派四隊</span>。</li>
				<li>比賽當日請選手攜帶學生證進場，以利身份確認。</li>
				<li>比賽方式以上機撰寫程式為主
				<ul class="unstyled" style="color:red;padding-left:20px;">
					<li>比賽使用語言：C/C++、JAVA</li>
					<li>編譯器版本：GCC4.6.1、JDK1.6以上</li>
					<li>比賽提供IDE：Dev C++ 5.1、NetBean 6.9以上、JCreator LE</li>
				</ul>若使用其他IDE軟體，請各隊伍自備安裝檔，比賽當天可在開放測試時間入場進行環境建置，惟各隊須自行承擔使用任何IDE造成之問題。</li>
				<li>比賽時間為<span style="color:red;"> 5 小時 ( 12:00~17:00 ) </span>，題目約8~10題不等，以英文出題。各隊可攜帶20張A4大小雙面印刷之參考資料及紙本字典在競賽時參考。但<span style="color:red;">不可攜帶機器可讀寫的任何軟體或資料，亦不可攜帶自己的電腦、終端機、手機、電子辭典或隨身碟，並嚴禁使用行動電話及呼叫器</span>，以免干擾其他隊伍作答，現場不提供網路連線，亦不得用任何方法上網查詢資料，違者將取消參賽資格。</li>
				<li>每題完成後請透過線上評分程式(Online-Judge)，上傳進行題目評測。</li>
				<li>評分以題數計，上傳成功暨完全答對(Accept)得 1 分，與正確答案較接近但非完全正確者仍不給分，以線上評分程式為準。</li>
				<li>評分以正確解題數目最多的隊伍為優勝，解題數相同時，以累計的解題時間較少者為優勝。</li>
				<li>請於報名成功後三天內完成繳費，未完成繳費隊伍視同報名無效，須重新線上報名。
			
					<ul class="unstyled" style="color:red;padding-left:20px;">
						<li>繳費流程</li>
						<li> 1.請至郵局購買「郵政匯票」(受款人：國立彰化師範大學)。</li>
						<li> 2.將「匯票」連同「基本資料」，一併以掛號郵寄至：500 彰化市師大路 2號 國立彰化師範大學 資工系 收(請於信封註明為報名 2015中區大專院校程式競賽報名資料)。</li>
						<li> 3.基本資料請務必填寫 "報名 2016中區大專院校程式競賽、繳款人姓名、隊伍及連絡電話</li>
						<li> ※報名費用為300元</li>
						<li> (※如未寄送匯票之隊伍不予受理。)</li>
					</ul>
				<li>參賽獎項 
					<ul class="unstyled">
						<li>第一名：禮卷六千元及獎狀乙紙。</li>
						<li>第二名：禮卷四千五百元及獎狀乙紙。</li>
						<li>第三名：禮卷三千元及獎狀乙紙。</li>
						<li>佳作三名：禮卷一千元及獎狀乙紙。</li>
					</ul>
				</li>
				<li>主辦單位保留一切權利。</li>
			
			</ol>
		</div>
